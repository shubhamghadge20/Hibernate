package mypack;

public class Product {
  
	private int pcode;
	private String pname;
	private float prate;
	public int getPcode() {
		return pcode;
	}
	public void setPcode(int pcode) {
		this.pcode = pcode;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public float getPrate() {
		return prate;
	}
	public void setPrate(float prate) {
		this.prate = prate;
	}
	
	
}
